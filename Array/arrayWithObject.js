//an array is an object, observe
const almostarray={0:'Ari',1:"Ariola",2:"Ashley",split:(ind1,ind2)=>{
    const obj={}
    let count=0
    almostarray.forEach((item)=>{
        if(item>=ind1 && item<=ind2){
            obj={count:this.item}
            count++
            item++
        }
        return obj
    })
}}